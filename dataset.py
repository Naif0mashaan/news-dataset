# 🔷 1. Import Libraries
import pandas as pd
import re
import nltk

from nltk.corpus import stopwords
from nltk.tokenize import word_tokenize
from nltk.stem import PorterStemmer, WordNetLemmatizer

import gensim
from gensim import corpora
from gensim.models import CoherenceModel

# Download NLTK resources (run only once)
nltk.download('punkt')
nltk.download('stopwords')
nltk.download('wordnet')


def main():

    # 🔷 2. Load Dataset
    df = pd.read_csv('news_dataset.csv')
    texts = df['text']

    # 🔷 3. Text Preprocessing
    texts = texts.dropna()

    stop_words = set(stopwords.words('english'))
    stemmer = PorterStemmer()
    lemmatizer = WordNetLemmatizer()

    processed_texts = []

    for text in texts:
        text = text.lower()
        text = re.sub(r'[^a-zA-Z\s]', '', text)

        tokens = word_tokenize(text)

        # Clean tokens
        tokens = [
            word for word in tokens 
            if word not in stop_words and len(word) > 2 and word.isalpha()
        ]

        tokens = [stemmer.stem(word) for word in tokens]
        tokens = [lemmatizer.lemmatize(word) for word in tokens]

        processed_texts.append(tokens)

    # 🔷 4. Dictionary and Corpus
    dictionary = corpora.Dictionary(processed_texts)
    dictionary.filter_extremes(no_below=5, no_above=0.5)

    corpus = [dictionary.doc2bow(text) for text in processed_texts]

    # 🔷 5. LDA Model
    lda_model = gensim.models.LdaModel(
        corpus=corpus,
        id2word=dictionary,
        num_topics=4,
        random_state=42,
        passes=15
    )

    # 🔷 6. Print Topics
    print("Topics Found:\n")
    for i, topic in lda_model.print_topics(num_words=8):
        print(f"Topic {i+1}: {topic}\n")

    # 🔷 7. Coherence Score
    coherence_model = CoherenceModel(
        model=lda_model,
        texts=processed_texts,
        dictionary=dictionary,
        coherence='c_v'
    )

    coherence_score = coherence_model.get_coherence()
    print(f"\nCoherence Score: {coherence_score:.4f}")


# 🔥 REQUIRED FOR WINDOWS (FIXES YOUR ERROR)
if __name__ == "__main__":
    main()


